package JavaConcepts;

public class VariablesAndDataTypes {
	
	public static void main(String[] args) {
		//Variables are containers for storing data values. 
		
		//DataType represent the type of the data
		
		int var;   //Variable declaration
		var = 20;  // value assignment
		
		//Primitive DataTypes
		
		int  i=10;  // Variable declaration with assignment
		
		byte b= 100;
		
		long l= 123489;
		
		float f= 15.234678567f;
		System.out.println(f);  //Prints the output to console
		
		double d= 20.53767669;
		System.out.println(d);
		
		char c= 'a';
		
		boolean bool= false;

		final int fixedValue=99;
		//fixedValue= 22; //Not possible to assaign again
	}

}

